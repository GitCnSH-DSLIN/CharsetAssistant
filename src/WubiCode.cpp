static bool _IsCJK(std::wstring ch)
{
	wchar_t uc;

	if (ch.length() > 1)
		return false;

	uc = ch.at(0);
	if (uc >= 0x4E00 && uc <= 0x9FFF)
		return true;

	return false;
}

